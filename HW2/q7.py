## Function that Xors two lists elements that are in the same index
def xorLists(first, second):
    newList = []
    length = len(first)
    if length != len(second):
        raise Exception("Lists should be same length!") 
    for idx in range(length):
        if (not type(first[idx]) is int) or (not type(first[idx]) is int):
            raise Exception("Types should be integer!")
        newList.append(int(first[idx] ^ second[idx]))
    return newList

def LFSR(C, S):
    L = len(S)
    ## Just to see the error exactly
    if len(C) - 1 != L:
        raise Exception("Connector's length should be +1 Seed's length")
    fb = 0
    out = S[L-1]
    for i in range(0, L):
        fb = fb ^ (S[i] & C[i+1])
    for i in range(L-1, 0, -1):
        S[i] = S[i-1]

    S[0] = fb
    return out
# To escape from pointer logic
def deepcopy(L):
    Ll = len(L)
    newL = []
    for i in range(0, Ll):
        newL.append(L[i])
    return newL
# Reciprocal function to make LFSR to RFSR
def connector_r(connector,L):
    newConnector = deepcopy(connector)
    for index in range(1, L):
        if newConnector[index] == 1: 
            newConnector[L - index] = 1 - newConnector[L - index]
    return newConnector
